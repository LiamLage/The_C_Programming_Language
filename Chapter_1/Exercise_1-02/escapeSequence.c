#include <stdio.h>

int main()  {
    printf("Tab:            \\t\n");    /* \t for Tab*/
    printf("Backspace:      \\b\n");    /* \b for Backspace */
    printf("backslash:      \\\\ \n");  /* \\ for Backslash */
    printf("Single quote:   \\'\n");    /* \' for Single quote */
    printf("Double quote:   \\\"\n");   /* \" for Double quote */
    return 0;
}